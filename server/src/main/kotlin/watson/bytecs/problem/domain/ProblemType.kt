package watson.bytecs.problem.domain

/**
 * 문제가 사용자에게 요구하는 사고의 종류.
 * 정답이 놓인 공간의 성질이 유형마다 달라, 근접(오탈자) 판정의 타당성이 갈린다.
 */
enum class ProblemType {

    /**
     * 지문이 정의를 주고, 사용자가 그 개념의 '이름'을 떠올린다. (정답 = 개념 이름)
     * 예: "가장 나중에 넣은 데이터가 먼저 나오는 후입선출(LIFO) 자료구조는?" → 스택
     *
     * 정답이 자연어 단어라, 개념명끼리는 편집거리가 멀다(`TCP`↔`UDP` = 2).
     * 따라서 편집거리가 1~2로 가깝다는 건 '다른 개념'이 아니라 오타라는 신호다(`collsion` → `collision`).
     */
    DEFINITION_RECALL,

    /**
     * 개념을 재료로 여러 단계를 거쳐 답을 도출한다. (정답 ≠ 개념 이름)
     * 예: "다음 코드의 시간 복잡도를 빅오 표기법으로?" → o(n^2)
     *
     * 정답이 수식·숫자처럼 밀집한 공간의 한 점이라, 이웃이 전부 '유효한 다른 답'이다.
     * 한 글자가 곧 의미(지수·차수·자릿수)이므로 편집거리가 작다는 건 오타가 아니라
     * 유도를 틀렸다는 뜻이다(`o(n²)`에 `o(n)` = 이중 반복문을 하나로 잘못 셈).
     */
    DERIVATION,
}
